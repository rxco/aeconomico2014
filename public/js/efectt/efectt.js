$(window).load(function(){
    var $container = $('.contenedor-result');
    $container.isotope({
        filter: '*',
        animationOptions: {
            duration: 750,
            easing: 'linear',
            queue: false
        }
    });
 
    $('.filtrar a').click(function(){
        $('.filtrar .actual').removeClass('actual');
        $(this).addClass('actual');
 
        var selector = $(this).attr('data-filter');
        $container.isotope({
            filter: selector,
            animationOptions: {
                duration: 750,
                easing: 'linear',
                queue: false
            }
         });
         return false;
    }); 
});